		self:addChild(self.sp5,-1)
		self:addChild(self.sp6,1)
		self.sp6:setRotation(-90)

		self.sp7 = cc.Sprite:createWithSpriteFrameName("YQS_dish.png")
		self.sp7:setPositionY(-m_BackWidth)
		self.sp8 = Fish.new(12)
		self.sp8:setPositionY(-m_BackWidth)
		self:addChild(self.sp7,-1)
