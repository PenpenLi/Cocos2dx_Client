function Dsyuan:play()
	local rotate = cc.RotateBy:create(3,-360)
    self.sp1:runAction(cc.RepeatForever:create(rotate))
    local rotate1 = cc.RotateBy:create(3,-360)
    self.sp3:runAction(cc.RepeatForever:create(rotate1))
    local rotate2 = cc.RotateBy:create(3,-360)
    self.sp5:runAction(cc.RepeatForever:create(rotate2))
    local rotate2 = cc.RotateBy:create(3,-360)
    self.sp7:runAction(cc.RepeatForever:create(rotate2))
end
function Dsyuan:stop1()
    self.sp1:stopAllActions()
    self.sp3:stopAllActions()
    self.sp5:stopAllActions()
